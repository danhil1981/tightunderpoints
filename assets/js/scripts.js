function show_players() {
    
    var a = document.getElementById("button_players");
    a.style.borderColor = "green";
    a.style.color = "white";
    a.style.backgroundColor = "green";

    var b = document.getElementById("button_raids");
    b.style.borderColor = "grey";
    b.style.color = "black";
    b.style.backgroundColor = "white";

    var c = document.getElementById("button_items");
    c.style.borderColor = "grey";
    c.style.color = "black";
    c.style.backgroundColor = "white";

    var d = document.getElementById("button_encounters");
    d.style.borderColor = "grey";
    d.style.color = "black";
    d.style.backgroundColor = "white";
    
    var e = document.getElementById("players");
    e.style.display = "block";

    var f = document.getElementById("raids");
    f.style.display = "none";

    var g = document.getElementById("items");
    g.style.display = "none";

    var h = document.getElementById("encounters");
    h.style.display = "none";
}

