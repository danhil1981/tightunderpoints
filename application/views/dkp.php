<div class="col-10 mx-auto">
    <h1 class="text-center h1_text my-5">DKP System</h1>
    <p class="text-white text-justify">
        The goal of the DKP system is to make loot distribution understandable, fast and fair.  The goal is for the people who show up the most to get the most loot, while still making sure that everyone has a chance to get some good stuff.
        Points, costs and many other things are tracked on the tightunderpoints webpage, linked in the resources section on Discord.
    </p>
        

    <h2 class="h2_text">Earning Points</h2>
    <p class="text-white text-justify">
        One point is earned for each boss kill during a pre-planned raid.  Additionally, the first kill on any new boss will earn a bonus point.  Earned points decay 50 days after they are earned.
        Points are earned separately for each character.  This ensures that loot is distributed fairly regardless of the number of characters played and also prevents a player from monopolizing loot on one character at the expense of their others.
        Smaller bosses may be killed on off nights if people are available and interested.  In order to be as fair as possible to those who aren't spending their time stalking discord, these kills will not earn points.
    </p>
    <h2 class="h2_text">Spending Points and Looting</h2>
    <p class="text-white text-justify">
        When an item drops, players will announce which item(s) they are interested in and which character they wish to loot on.  A /random will then be done between the character with the most points and anyone within two points of them and the winner will pay the point cost for the item.  
        For example, suppose a 4 point item drops and three players are interested; LotLizard with 10 points, SheepLover with 8 points and ErbenZaehler with 6 points.  LotLizard has the most points and SheepLover is within 2 points of them, so they are eligible.  ErbenZaehler is not within 2 points of player1, so they are not eligible.  Whoever is handling loot will announce "LotLizard - 1, SheepLover - 2" then do a /random 1 2 to determine the winner.
        Item costs will decay 50 days after they are looted.  Combined with the decay on points earned this means all looting is based on a 50 day (~6 week) moving window.
        If a boss kill isn't worth a point, whether because it is being killed on an off night or it is too small to earn a point (i.e. glyphed serpent or Rhag1), then its loot will also not cost points.  Additionally, some items are just so bad that we can't justify even a 1 point cost for them.  In either of these cases, the loot will be randomed without considering DKP.
    </p>
    <h2 class="h2_text">DKP costs</h2>
    <p class="text-white text-justify">
        Items have a cost between 0-5 points.  In general, point costs are chosen in order to keep the cost of gearing different types of characters relatively similar while also ensuring that the most desirable items cost the most.  For example, big FT armor is expensive, but 'stat only' caster armor is cheap compared to 'stat only' armor which is shared with melee.  Melee should pay a high cost for weapons and an average cost for most armor.  Casters should pay a high cost for a half dozen important pieces of armor with FT and focus effects but a low cost for the rest.
        Point values will be updated to keep pace with progression.
    </p>
    <h2 class="h2_text">Mains, Alts and Bots</h2>
    <p class="text-white text-justify">
        Every player has up to 3 dedicated main characters who will earn and spend DKP normally.
        Alternate characters may be played on raids if needed (or just logged out in raid zones) and collect loot, but they will not be allowed to win over mains, regardless of DKP.  Unless the owner specifies otherwise, DKP earned while playing an alt will go to the player's main characters.
        Bots are a special third category of shared guild characters.  They have the lowest priority.
        It is possible to change your main character, however points are not transferable between characters. We reserve the right to restrict main changes if we see people switching too frequently.
    </p>
    <h2 class="h2_text">Gaming the system</h2>
    <p class="text-white text-justify">
        This system was built with simplicity as a consideration.  While we tried to address them, we're sure there are ways to game the system.  Trying to abuse the system is a dick move.  Don't do it.  If you're too successful at it we'll just change the rules to close whatever loophole you're abusing anyway.
        That said, if you feel like the system is not achieving the goals we've laid out don't hesitate to raise any concerns.  Often there are trade-offs and a judgement call has to be made, but we're trying to make it as understandable, fast and fair as possible.
    </p>
    <h2 class="h2_text">Rewards/Costs and Standings</h2>
    <p class="text-white text-justify">
        All costs and rewards are posted to the members section of the website. You can find them in the Bosses and Items tables.
        The points section of the website shows you the current standing of points and also lets you compare individual members.
    </p>
